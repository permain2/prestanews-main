  /**
   * Generate an Astro page from article data
   */
  generatePage(article) {
    try {
      // Determine output path based on category
      const categoryDir = path.join(this.pagesDir, article.category);
      if (!fs.existsSync(categoryDir)) {
        fs.mkdirSync(categoryDir, { recursive: true });
      }

      const filePath = path.join(categoryDir, `${article.slug}.astro`);
      const pageContent = this._generateAstroContent(article);

      fs.writeFileSync(filePath, pageContent);

      return {
        success: true,
        filePath,
        relativePath: `src/pages/${article.category}/${article.slug}.astro`
      };
    } catch (error) {
      return {
        success: false,
        error: error.message
      };
    }
  }

  /**
   * Generate Astro file content
   */
  _generateAstroContent(article) {
    const providersJson = JSON.stringify(article.providers || [], null, 2)
      .split('\n')
      .map((line, i) => i === 0 ? line : '  ' + line)
      .join('\n');

    const faqsJson = JSON.stringify(article.faqs || [], null, 2)
      .split('\n')
      .map((line, i) => i === 0 ? line : '  ' + line)
      .join('\n');

    // Escape backticks in content for template literal
    const escapedContent = (article.content || '')
      .replace(/`/g, '\\`')
      .replace(/\$/g, '\\$');

    const tocItems = this._generateTocItems(article);

    return `---
/**
 * ${article.title}
 * Generated by Screened Admin Dashboard
 * Last updated: ${new Date().toISOString()}
 */
import ComparisonLayout from '../../layouts/ComparisonLayout.astro';

// Provider data
const providers = ${providersJson};

// FAQ data
const faqs = ${faqsJson};

// TOC items
const tocItems = ${JSON.stringify(tocItems, null, 2)};

// SEO content (markdown)
const content = \`${escapedContent}\`;
---

<ComparisonLayout
  title="${this._escapeHtml(article.title)}"
  description="${this._escapeHtml(article.description)}"
  category="${article.category}"
  categoryLabel="${this._getCategoryLabel(article.category)}"
  heroImage="${article.heroImage || '/insurance-heroes/default.jpeg'}"
  author="${article.author || 'Screened Editorial Team'}"
  date="${new Date().toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric' })}"
  providers={providers}
  faqs={faqs}
  researchIntro="${this._escapeHtml(article.researchIntro || '')}"
  tocItems={tocItems}
  content={content}
/>
`;
  }

  /**
   * Generate TOC items from article content
   */
  _generateTocItems(article) {
    const items = [
      { id: 'providers', label: 'Top Picks', href: '#providers' }
    ];

    // Extract h2 headings from content for TOC
    if (article.content) {
      const h2Matches = article.content.match(/^## (.+)$/gm);
      if (h2Matches) {
        h2Matches.forEach(match => {
          const text = match.replace(/^## /, '');
          const id = text.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
          items.push({ id, label: text, href: `#${id}` });
        });
      }
    }

    if (article.faqs && article.faqs.length > 0) {
      items.push({ id: 'faq', label: 'FAQ', href: '#faq' });
    }

    return items;
  }

