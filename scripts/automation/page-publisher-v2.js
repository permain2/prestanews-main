  /**
   * Simple markdown parser
   */
  _parseMarkdown(markdown) {
    if (!markdown) return '';
    
    let html = markdown
      // Headers
      .replace(/^### (.+)$/gm, '<h3>$1</h3>')
      .replace(/^## (.+)$/gm, '<h2 id="$1">$1</h2>')
      // Bold
      .replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
      // Italic
      .replace(/\*(.+?)\*/g, '<em>$1</em>')
      // Lists
      .replace(/^- (.+)$/gm, '<li>$1</li>')
      // Wrap lists
      .replace(/(<li>.*<\/li>\n?)+/g, '<ul>$&</ul>')
      // Paragraphs
      .replace(/^(?!<[hulo]|<div)(.+)$/gm, '<p>$1</p>')
      // Fix IDs
      .replace(/id="([^"]+)"/g, (match, p1) => {
        return `id="${p1.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '')}"`;
      });
    
    // Handle special boxes
    html = html.replace(/<div class="key-takeaways">([\s\S]*?)<\/div>/gi, (match) => match);
    html = html.replace(/<div class="info-box">([\s\S]*?)<\/div>/gi, (match) => match);
    
    return html;
  }

  /**
   * Escape HTML entities
   */
  _escapeHtml(str) {
    if (!str) return '';
    return str
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&#039;');
  }

  /**
   * Get category label from slug
   */
  _getCategoryLabel(category) {
    const labels = {
      'credit-cards': 'CREDIT CARDS',
      'insurance': 'INSURANCE',
      'finance': 'FINANCE',
      'tech': 'TECHNOLOGY',
      'home': 'HOME',
      'lifestyle': 'LIFESTYLE'
    };
    return labels[category] || category.toUpperCase();
  }
